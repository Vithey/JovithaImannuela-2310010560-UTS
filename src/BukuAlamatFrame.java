    // Implementasi Impor (Import) dari CSV (Sederhana)
    private void importData() {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Pilih File CSV untuk Impor");
        int userSelection = fileChooser.showOpenDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToOpen = fileChooser.getSelectedFile();
            int importedCount = 0;
            String sql = "INSERT INTO kontak (nama, telepon, email, alamat, properti, keterangan) VALUES (?, ?, ?, ?, ?, ?)";

            try (BufferedReader br = new BufferedReader(new FileReader(fileToOpen));
                 PreparedStatement pstmt = koneksi.prepareStatement(sql)) {
                
                String line;
                boolean isHeader = true;
                while ((line = br.readLine()) != null) {
                    if (isHeader) {
                        isHeader = false; // Lewati baris header
                        continue;
                    }

                    // Pemisahan sederhana (mungkin bermasalah jika ada koma dalam data)
                    String[] data = line.split(",", -1); 
                    
                    // Asumsi urutan data: ID,Nama,Telepon,Email,Alamat,Properti,Keterangan
                    // ID (index 0) diabaikan karena auto-increment
                    if (data.length >= 7) {
                        pstmt.setString(1, data[1]); // Nama
                        pstmt.setString(2, data[2]); // Telepon
                        pstmt.setString(3, data[3]); // Email
                        pstmt.setString(4, data[4]); // Alamat
                        pstmt.setString(5, data[5]); // Properti
                        pstmt.setString(6, data[6]); // Keterangan
                        pstmt.addBatch(); // Tambahkan ke batch
                        importedCount++;
                    }
                }
                
                pstmt.executeBatch(); // Eksekusi semua INSERT
                JOptionPane.showMessageDialog(this, "Impor " + importedCount + " data berhasil!");
                loadData();
            } catch (IOException | SQLException e) {
                JOptionPane.showMessageDialog(this, "Gagal impor data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }
    private boolean isKontakExists(String nama, String telepon) throws SQLException {
        // Query: Hitung berapa baris yang punya Nama DAN Telepon ini
        String sql = "SELECT COUNT(*) FROM kontak WHERE nama = ? AND telepon = ?";
        try (PreparedStatement pstmt = koneksi.prepareStatement(sql)) {
            pstmt.setString(1, nama);
            pstmt.setString(2, telepon);
            
            try (java.sql.ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    // Jika count > 0, data sudah ada
                    return rs.getInt(1) > 0; 
                }
            }
        }
        return false;
    }
    
