    private void loadData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("Telepon");
        model.addColumn("Email");
        model.addColumn("Properti");
        model.addColumn("Keterangan");
        
        // Mengubah nama kolom default NetBeans (Title 1, Title 2, ...)
        tblBuku.setModel(model);

        String sql = "SELECT id, nama, telepon, email, properti, keterangan FROM kontak";
        
        try (Statement stmt = koneksi.createStatement();
             ResultSet rs = stmt.executeQuery(sql)) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nama"),
                    rs.getString("telepon"),
                    rs.getString("email"),
                    rs.getString("properti"),
                    rs.getString("keterangan") // <-- AMBIL DATA KETERANGAN
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }

