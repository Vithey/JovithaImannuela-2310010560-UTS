    private void tblBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBukuMouseClicked
                                     
        int baris = tblBuku.getSelectedRow();
        if (baris == -1) return; 

        // Ambil ID dari kolom pertama (index 0)
        selectedId = (int) tblBuku.getValueAt(baris, 0);

        // Ambil data lengkap dari database berdasarkan ID
        String sql = "SELECT * FROM kontak WHERE id = ?";
        try (PreparedStatement pstmt = koneksi.prepareStatement(sql)) {
            pstmt.setInt(1, selectedId);
            try (ResultSet rs = pstmt.executeQuery()) {
                if (rs.next()) {
                    txtNama.setText(rs.getString("nama"));
                    txtTelp.setText(rs.getString("telepon"));
                    txtEmail.setText(rs.getString("email"));
                    txtAlamat.setText(rs.getString("alamat"));
                    txtKeterangan.setText(rs.getString("keterangan"));
                    cmbProperti.setSelectedItem(rs.getString("properti"));
                    
                    // Aktifkan tombol Update dan Delete
                    btnSave.setEnabled(false);
                    btnUpdate.setEnabled(true);
                    btnDelete.setEnabled(true);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil detail data: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tblBukuMouseClicked

