    private void btnImporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImporActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Pilih File untuk Impor");
        
        // --- BUAT FILTER BARU ---
        javax.swing.filechooser.FileNameExtensionFilter txtFilter = 
                new javax.swing.filechooser.FileNameExtensionFilter("Text Files (*.txt)", "txt");
        javax.swing.filechooser.FileNameExtensionFilter pdfFilter = 
                new javax.swing.filechooser.FileNameExtensionFilter("PDF Files (*.pdf)", "pdf");
        
        fileChooser.addChoosableFileFilter(txtFilter);
        fileChooser.addChoosableFileFilter(pdfFilter);
        fileChooser.setFileFilter(txtFilter); // Default ke TXT
        
        int userSelection = fileChooser.showOpenDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToOpen = fileChooser.getSelectedFile();
            
            javax.swing.filechooser.FileNameExtensionFilter currentFilter = 
                    (javax.swing.filechooser.FileNameExtensionFilter) fileChooser.getFileFilter();

            // --- PANGGIL METODE SESUAI FILTER ---
            if (currentFilter.getDescription().equals("Text Files (*.txt)")) {
                importDataFromTXT(fileToOpen);
            } else if (currentFilter.getDescription().equals("PDF Files (*.pdf)")) {
                importDataFromPDF(fileToOpen);
            }
        }
    }//GEN-LAST:event_btnImporActionPerformed

    private void btnEksporActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEksporActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Simpan Data Kontak");
        
        // --- BUAT FILTER BARU ---
        javax.swing.filechooser.FileNameExtensionFilter txtFilter = 
                new javax.swing.filechooser.FileNameExtensionFilter("Text Files (*.txt)", "txt");
        javax.swing.filechooser.FileNameExtensionFilter pdfFilter = 
                new javax.swing.filechooser.FileNameExtensionFilter("PDF Files (*.pdf)", "pdf");
        
        fileChooser.addChoosableFileFilter(txtFilter);
        fileChooser.addChoosableFileFilter(pdfFilter);
        
        int userSelection = fileChooser.showSaveDialog(this);

        if (userSelection == JFileChooser.APPROVE_OPTION) {
            File fileToSave = fileChooser.getSelectedFile();
            String path = fileToSave.getAbsolutePath();
            
            javax.swing.filechooser.FileNameExtensionFilter currentFilter = 
                    (javax.swing.filechooser.FileNameExtensionFilter) fileChooser.getFileFilter();

            // --- PANGGIL METODE SESUAI FILTER ---
            if (currentFilter.getDescription().equals("Text Files (*.txt)")) {
                if (!path.toLowerCase().endsWith(".txt")) {
                    fileToSave = new File(path + ".txt");
                }
                exportDataToTXT(fileToSave);
            } else if (currentFilter.getDescription().equals("PDF Files (*.pdf)")) {
                 if (!path.toLowerCase().endsWith(".pdf")) {
                    fileToSave = new File(path + ".pdf");
                }
                exportDataToPDF(fileToSave);
            }
            // Tambahkan logika untuk format lain (CSV/JSON) jika diperlukan
        }
    }//GEN-LAST:event_btnEksporActionPerformed

