    private void exportDataToPDF(File fileToSave) {
        Document document = new Document(PageSize.A4.rotate()); // Menggunakan A4 landscape
        String sql = "SELECT id, nama, telepon, email, properti, keterangan FROM kontak";

        try {
            PdfWriter.getInstance(document, new FileOutputStream(fileToSave));
            document.open();

            // 1. Judul Dokumen
            document.add(new Paragraph("APLIKASI BUKU ALAMAT"));
            document.add(new Paragraph("Laporan Data Kontak"));
            document.add(new Paragraph("\n"));

            // 2. Buat Tabel PDF (6 Kolom)
            PdfPTable table = new PdfPTable(6); 
            table.setWidthPercentage(100); // Lebar tabel 100% dari halaman
            float[] columnWidths = {0.5f, 2f, 2f, 3f, 1.5f, 3f};
            table.setWidths(columnWidths);

            // Fungsi helper untuk Cell Header
            PdfPCell headerCell;
            String[] headers = {"ID", "NAMA", "TELEPON", "EMAIL", "PROPERTI", "KETERANGAN"};
            for (String header : headers) {
                headerCell = new PdfPCell(new Phrase(header));
                headerCell.setHorizontalAlignment(Element.ALIGN_CENTER);
                table.addCell(headerCell);
            }

            // 3. Isi Data dari Database
            try (Statement stmt = koneksi.createStatement();
                 ResultSet rs = stmt.executeQuery(sql)) {

                while (rs.next()) {
                    // ID
                    PdfPCell idCell = new PdfPCell(new Phrase(String.valueOf(rs.getInt("id"))));
                    idCell.setHorizontalAlignment(Element.ALIGN_CENTER);
                    table.addCell(idCell);
                    
                    // Sisa kolom (Nama, Telepon, Email, Properti, Keterangan)
                    table.addCell(rs.getString("nama"));
                    table.addCell(rs.getString("telepon"));
                    table.addCell(rs.getString("email"));
                    table.addCell(rs.getString("properti"));
                    table.addCell(rs.getString("keterangan"));
                }
            }
            
            document.add(table);
            document.close();
            JOptionPane.showMessageDialog(this, "Ekspor PDF berhasil ke: " + fileToSave.getAbsolutePath());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal ekspor data ke PDF: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void importDataFromPDF(File fileToOpen) {
    int importedCount = 0;
    // Deklarasi PDDocument di luar blok try agar bisa diakses di blok finally
    PDDocument document = null; 
    
    // SQL Statement untuk memasukkan data baru
    String sql = "INSERT INTO kontak (nama, telepon, email, alamat, properti, keterangan) VALUES (?, ?, ?, ?, ?, ?)";
    
    try { 
        // --- KOREKSI UNTUK PDFBOX 2.x ---
        // Panggilan sederhana tanpa MemoryUsageSetting
        document = PDDocument.load(fileToOpen); 
        
        // 1. Ekstrak Semua Teks
        PDFTextStripper stripper = new PDFTextStripper();
        String text = stripper.getText(document);
        
        // 2. Analisis dan Parsing Teks
        String[] lines = text.split("\\n"); 
        
        try (java.sql.PreparedStatement pstmt = koneksi.prepareStatement(sql)) {
            
            for (String line : lines) {
                // Logika Parsing Teks (MENGASUMSIKAN format data terstruktur)
                // Peringatan: Logika parsing teks dari PDF sangat bergantung pada tata letak file sumber.
                
                // Contoh Parsing Sederhana (Menggunakan Koma sebagai pemisah di dalam baris)
                // Ini hanya ilustrasi. Anda mungkin perlu Regex atau pola yang lebih kompleks.
                String[] data = line.split(",", -1);
                
                // Asumsi data array [Nama, Telepon, Email, Alamat, Properti, Keterangan]
                if (data.length >= 6) { 
                    pstmt.setString(1, data[0].trim()); // Nama
                    pstmt.setString(2, data[1].trim()); // Telepon
                    pstmt.setString(3, data[2].trim()); // Email
                    pstmt.setString(4, data[3].trim()); // Alamat
                    pstmt.setString(5, data[4].trim()); // Properti
                    pstmt.setString(6, data[5].trim()); // Keterangan
                    pstmt.addBatch();
                    importedCount++;
                }
            }
            pstmt.executeBatch();
        }

        JOptionPane.showMessageDialog(this, "Impor " + importedCount + " data dari PDF selesai. Cek data.");
        loadData();

    } catch (IOException e) { 
        // Tangani masalah IO, termasuk kegagalan load() atau InvalidPasswordException
        JOptionPane.showMessageDialog(this, "Gagal membaca file PDF (IO/Sandi Error): " + e.getMessage(), "Error PDF", JOptionPane.ERROR_MESSAGE);
    } catch (SQLException e) {
        // Tangani masalah SQL
        JOptionPane.showMessageDialog(this, "Database Error saat impor PDF: " + e.getMessage(), "Error SQL", JOptionPane.ERROR_MESSAGE);
    } finally {
        // Blok ini memastikan dokumen PDF selalu ditutup
        if (document != null) {
            try {
                document.close();
            } catch (IOException closeEx) {
                // Abaikan error saat menutup dokumen
            }
        }
    }
}
    
